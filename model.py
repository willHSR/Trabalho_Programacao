#Fórmulas usada para cálculos

medida1 = input("digite a medida de origem do sulco:")
valor_medida_origem = int(medida1)

medida2 = input("digite a medida atual do sulco:")
valor_medida_atual = int(medida2)

medida_percentual_do_sulco = (valor_medida_atual/valor_medida_origem)*100
print(str(medida_percentual_do_sulco) + ' % ' )






